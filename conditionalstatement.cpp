// C++ program to print alphabets
#include <iostream>
using namespace std;

// Function to print the alphabet
// in lower case
void lowercaseAlphabets()
{
    // lowercase

    for (char c = 'a'; c <= 'z'; ++c)
        cout << c << " ";

    cout << endl;
}
// Function to print the alphabet
// in upper case
void uppercaseAlphabets()
{

    // uppercase
    for (char c = 'A'; c <= 'Z'; ++c)
        cout << c << " ";

    cout << endl;
}



